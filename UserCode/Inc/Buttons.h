/**
  * @brief  KEYCAN Button State Enumeration
  */
typedef enum
{
	KEY_NOT_PRESSED		= 0b00000000U,			/*!< Key Not Pressed */
	KEY_PRESSED			= 0b00000001U,			/*!< Key Pressed */
	KEY_HOLD_1			= 0b00000010U,			/*!< Hold 1 */
	KEY_HOLD_2			= 0b00000011U,			/*!< Hold 2 */
} KEYCAN_ButtonStateTypeDef;



/**
  * @brief  Button Structure Definition
  */
typedef struct __ButtonTypeDef
{
	GPIO_TypeDef* const					Port;

	uint16_t const						Pin;

	bool const							IsNegative;

	bool								Value;

	uint32_t							HoldCounter;

	KEYCAN_ButtonStateTypeDef			State;

} ButtonTypeDef;



/**
  * @brief  Buttons Structure Definition
  */
typedef struct __ButtonsTypeDef
{
	ButtonTypeDef						Key1;			/*!<  */

	ButtonTypeDef						Key2;			/*!<  */

	ButtonTypeDef						Key3;			/*!<  */

	ButtonTypeDef						Key4;			/*!<  */

} ButtonsTypeDef;



